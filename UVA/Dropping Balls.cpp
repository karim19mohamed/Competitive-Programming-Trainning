const int N = 1048575 + 9, M = 20 - 9;
vector<int> tree[M];

void solve() {
	int depth, ball;
	scanf("%d%d", &depth, &ball);

	printf("%d\n", tree[depth][ball - 1]);
}
int main() {
	_init_();
	tree[1].push_back(1);
	for (int d = 2; d < 21; ++d) {
		for (auto v : tree[d - 1])
			tree[d].push_back(v * 2);
		int sz = tree[d].size();
		for (int i = 0; i < sz; ++i)
			tree[d].push_back(tree[d][i] + 1);
	}
	int t;
	while (1) {
		scanf("%d", &t);
		if (t == -1) break;
		while (t--) {
			solve();
		}
	}
	return 0;
}
